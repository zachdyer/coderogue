function createMapSeed (chunkX: number, chunkY: number) {
    seedString = "chunkX:" + chunkX + "chunkY:" + chunkY
    mapSeed = 0
    for (let i = 0; i <= seedString.length - 1; i++) {
        mapSeed += seedString.charCodeAt(i)
    }
    return mapSeed
}
