function isRockSmashed (col: number, row: number) {
    if (chunkList[mapSeed]) {
        smashedRocks = chunkList[mapSeed].split(",")
        for (let value of smashedRocks) {
            if (col == parseFloat(value.split(" ")[0]) && row == parseFloat(value.split(" ")[1])) {
                return true
            }
        }
    }
    return false
}
scene.onOverlapTile(SpriteKind.Player, img`border`, function (sprite, location) {
    if (sprite.left < 16) {
        sprite.right = 16 * 15
        chunkX = chunkX - 1
    }
    if (sprite.right > 16 * 15) {
        sprite.left = 16
        chunkX = chunkX + 1
    }
    if (sprite.top < 16) {
        sprite.bottom = 16 * 15
        chunkY = chunkY + 1
    }
    if (sprite.bottom > 16 * 15) {
        sprite.top = 16
        chunkY = chunkY - 1
    }
    generateMap()
})
